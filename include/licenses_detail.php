<p style="text-align: justify;">
	 5.4. Обработка персональных данных производится Оператором при помощи следующих действий: сбор, запись, систематизация, накопление, хранение, уточнение (обновление, изменение), извлечение, использование, передача (предоставление, доступ), блокирование, удаление, уничтожение персональных данных.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Оператор не сообщает третьим лицам личную (персональную) информацию о своих клиентах, кроме случаев, предписанных Федеральным законом от 27.07.2006 № 152-ФЗ «О персональных данных», или когда клиент добровольно соглашается на передачу информации.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5.5. Оператор осуществляет как автоматизированную, так и неавтоматизированную обработку персональных данных, а также применяет смешанные формы обработки.&nbsp;
